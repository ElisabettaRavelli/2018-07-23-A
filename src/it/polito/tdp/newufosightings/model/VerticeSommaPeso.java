package it.polito.tdp.newufosightings.model;

public class VerticeSommaPeso {
	
	private String vertice;
	private Integer sommaPeso;
	public VerticeSommaPeso(String vertice, Integer sommaPeso) {
		super();
		this.vertice = vertice;
		this.sommaPeso = sommaPeso;
	}
	public String getVertice() {
		return vertice;
	}
	public Integer getSommaPeso() {
		return sommaPeso;
	}
	
	

}
