package it.polito.tdp.newufosightings.model;

public class ArcoPeso {
	
	private String v1;
	private String v2;
	private Double peso;
	public ArcoPeso(String v1, String v2, Double peso) {
		super();
		this.v1 = v1;
		this.v2 = v2;
		this.peso = peso;
	}
	public String getV1() {
		return v1;
	}
	public String getV2() {
		return v2;
	}
	public Double getPeso() {
		return peso;
	}
	
	

}
